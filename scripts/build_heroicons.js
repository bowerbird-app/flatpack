#!/usr/bin/env node
// Generates app/javascript/flat_pack/heroicons.js from the heroicons npm package.
// Run with: npm run build:heroicons

"use strict";

const fs = require("fs");
const path = require("path");

const heroiconsDir = path.join(__dirname, "../node_modules/heroicons/24");
const outputFile = path.join(
  __dirname,
  "../app/javascript/flat_pack/heroicons.js"
);

const BUILD_TARGETS = [
  { exportName: "outline", pathParts: ["24", "outline"] },
  { exportName: "solid", pathParts: ["24", "solid"] },
  { exportName: "mini", pathParts: ["20", "solid"] },
  { exportName: "micro", pathParts: ["16", "solid"] },
];

function extractInner(svgContent) {
  // Strip the outer <svg ...> wrapper (opening tag may span one line)
  const withoutOpen = svgContent.replace(/^<svg[^>]*>\n?/s, "");
  const withoutClose = withoutOpen.replace(/\n?<\/svg>\s*$/s, "");
  return withoutClose.trim();
}

function buildVariant(exportName, pathParts) {
  const dir = path.join(__dirname, "../node_modules/heroicons", ...pathParts);
  const files = fs
    .readdirSync(dir)
    .filter((f) => f.endsWith(".svg"))
    .sort();

  const entries = files.map((file) => {
    const name = file.replace(/\.svg$/, "");
    const content = fs.readFileSync(path.join(dir, file), "utf8");
    const inner = extractInner(content);
    // Escape backticks to avoid breaking template literals (SVG paths shouldn't
    // contain them, but be defensive). Dollar signs followed by { must be escaped.
    const escaped = inner.replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
    return `  "${name}": \`${escaped}\``;
  });

  return `export const ${exportName} = {\n${entries.join(",\n")},\n};`;
}

const lines = [
  "// Heroicons v2 — MIT License — https://heroicons.com",
  "// Full icon set — generated by scripts/build_heroicons.js",
  "// To regenerate: npm run build:heroicons",
  "",
  ...BUILD_TARGETS.flatMap(({ exportName, pathParts }) => [buildVariant(exportName, pathParts), ""]),
  "",
];

fs.writeFileSync(outputFile, lines.join("\n"));
console.log(`Written ${outputFile}`);

// Quick sanity check (file uses ESM exports, so verify via text search)
const generated = fs.readFileSync(outputFile, "utf8");
const missing = [];
["bug-ant", "chart-bar"].forEach((name) => {
  if (!generated.includes(`"${name}":`)) missing.push(name);
});
if (missing.length > 0) {
  console.error("MISSING icons:", missing.join(", "));
  process.exit(1);
}
// Count icon entries: each ends with a backtick-comma or backtick-newline-}
const outlineSection = generated.match(/export const outline = \{([\s\S]*?)\};/)?.[1] || "";
const solidSection = generated.match(/export const solid = \{([\s\S]*?)\};/)?.[1] || "";
const miniSection = generated.match(/export const mini = \{([\s\S]*?)\};/)?.[1] || "";
const microSection = generated.match(/export const micro = \{([\s\S]*?)\};/)?.[1] || "";
const countEntries = (s) => (s.match(/^\s+"/gm) || []).length;
console.log(`outline["bug-ant"] present, outline["chart-bar"] present`);
console.log(`Total outline icons: ${countEntries(outlineSection)}`);
console.log(`Total solid icons: ${countEntries(solidSection)}`);
console.log(`Total mini icons: ${countEntries(miniSection)}`);
console.log(`Total micro icons: ${countEntries(microSection)}`);
